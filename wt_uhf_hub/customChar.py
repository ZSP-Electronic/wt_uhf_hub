def uparrow():
    up = bytearray([0x04,0x0E,0xFF,0x04,0x1C,0x00,0x00,0x00])
    return up
    
def rightarrow():
    right = bytearray([0x00,0x04,0x02,0xFF,0x02,0x04,0x00,0x00])
    return right
    
def numBLK(C):
    if C == 'TLoff':
        Corner = bytearray([0x00,0x00,0x00,0x07,0x0c,0x18,0x10,0x10])
    if C == 'TRoff':
        Corner = bytearray([0x00,0x00,0x00,0x1c,0x06,0x03,0x01,0x01])
    if C == 'BLoff':
        Corner = bytearray([0x10,0x10,0x18,0x0c,0x07,0x00,0x00,0x00])
    if C == 'BRoff':
        Corner = bytearray([0x01,0x01,0x03,0x06,0x1c,0x00,0x00,0x00])
        
    if C == 'TLon':
        Corner = bytearray([0x00,0x00,0x00,0x07,0x0c,0x19,0x13,0x17])
    if C == 'Tron':
        Corner = bytearray([0x00,0x00,0x00,0x1c,0x06,0x13,0x19,0x1d])
    if C == 'BLon':
        Corner = bytearray([0x17,0x13,0x19,0x0c,0x07,0x00,0x00,0x00])
    if C == 'BRon':
        Corner = bytearray([0x1d,0x19,0x13,0x06,0x1c,0x00,0x00,0x00])
    return Corner
    
def RecordSym(sec):
    if sec == "offleft":
        section = bytearray([0x00,0x03,0x04,0x04,0x04,0x04,0x03,0x00])
    if sec == "offright":
        section = bytearray([0x00,0x18,0x04,0x04,0x04,0x04,0x18,0x00])
    if sec == "onleft":
        section = bytearray([0x00,0x03,0x04,0x05,0x05,0x04,0x03,0x00])
    if sec == "onright":
        section = bytearray([0x00,0x18,0x04,0x14,0x14,0x04,0x18,0x00])
    return section
