def RecordSym(sec):
    if sec == "offSym":
        section = bytearray([0x0E,0x11,0x11,0x11,0x11,0x11,0x11,0x0E])
    if sec == "onSym":
        section = bytearray([0x0E,0x11,0x15,0x15,0x15,0x15,0x11,0x0E])
    if sec == "internetOn":
        section = bytearray([0x1F,0x04,0x04,0x04,0x04,0x04,0x04,0x1F])
    if sec == "internetOff":
        section = bytearray([0x1F,0x0E,0x15,0x15,0x15,0x15,0x0E,0x1F])
    return section
    
